inline const lv_font_t *font_sm()         { return &lv_font_montserrat_14; }
inline const lv_font_t *font_display_lg() { return &lv_font_montserrat_32; }
inline const lv_font_t *font_display_md() { return &lv_font_montserrat_24; }
